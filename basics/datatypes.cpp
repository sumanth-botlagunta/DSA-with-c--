#include <iostream>
using namespace std;

int main(){
    int i;
    char c;
    float f;
    double d;
    bool b;

    cout << "int: " << sizeof(i) << endl;
    cout << "char: " << sizeof(c) << endl;
    cout << "float: " << sizeof(f) << endl;
    cout << "double: " << sizeof(d) << endl;
    cout << "bool: " << sizeof(b) << endl;
}